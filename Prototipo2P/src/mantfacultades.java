    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        //Codigo que permite actualizar registros en la base de datos
        try {
            String Codigo = codfacul.getText().trim();

            java.sql.Connection cn = DriverManager.getConnection("jdbc:mysql://localhost:3307/siu", "root", "");
            PreparedStatement pst = cn.prepareStatement("update facultades set nombre_facultad = ?, estatus_facultad = ? where codigo_facultad = " + Codigo);

            pst.setString(1, nofacul.getText().trim());
            pst.setString(2, estafacul.getText().trim());
            pst.executeUpdate();

            label_status.setText("Modificaci√≥n exitosa");

        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton3ActionPerformed

