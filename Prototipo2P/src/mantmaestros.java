    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //Codigo que permite consultar registros en la base de datos
        try{
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost:3307/siu", "root", "");
            PreparedStatement pst = cn.prepareStatement("select * from maestros where codigo_maestro = ?");
            pst.setString(1, txt_carnet.getText().trim());
            
            ResultSet rs = pst.executeQuery();

            if(rs.next()){
            
                nombre.setText(rs.getString("nombre_maestro"));
                direccion.setText(rs.getString("direccion_maestro"));
                telefono.setText(rs.getString("telefono_maetro"));
                email.setText(rs.getString("email_maestro"));
                estatus.setText(rs.getString("estatus_maestro"));
                
            } else {
                JOptionPane.showMessageDialog(null, "NO ESTA REGISTRADO");
            }

        }catch (Exception e){

        }
    }//GEN-LAST:event_jButton2ActionPerformed

